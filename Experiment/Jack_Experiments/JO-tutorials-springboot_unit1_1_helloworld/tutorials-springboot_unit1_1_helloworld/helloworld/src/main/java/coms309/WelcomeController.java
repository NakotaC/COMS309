package coms309;

import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RestController;
import org.springframework.web.bind.annotation.PathVariable;

@RestController
class WelcomeController {

    @GetMapping("/")
    public String welcome() {
        return "Hello and welcome to demo 1";
    }

    @GetMapping("/{name}")
    public String welcome(@PathVariable String name) {
        return "Hello and welcome to to demo 1: " + name;
    }

    @GetMapping("/{name}/more")
    public String welcometmp(@PathVariable String name) {
        return "Hello " + name + ", this is another page generated by traveling further into the unknown, how is demo 1 going?";
    }

}
